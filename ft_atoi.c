int	ft_atoi(const char *str)
{
	int		x;
	int		neg;
	long	nb;

	x = 0;
	neg = 0;
	nb = 0;
	while (str[x] == '\t' || str[x] == '\v'
			|| str[x] == '\n' || str[x] == '\r' || str[x] == '\f'
			|| str[x] == ' ')
		x++;
	if (str[x] == '-')
		neg++;
	if (str[x] == '-' || str[x] == '+')
		x++;
	while (str[x] >= '0' && str[x] <= '9')
	{
		if ((nb > ((9223372036854775807 / 10) - str[x]))
		|| (nb < ((-9223372036854775807 + 1) / 10) - str[x]))
			return (neg > 0 ? -1 : 0);
		nb = nb * 10 + (str[x] - '0');
		x++;
	}
	return (neg > 0 ? (int)-nb : (int)nb);
}
